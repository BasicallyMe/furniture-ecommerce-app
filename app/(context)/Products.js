/**
 * Gets a single product by ID.
 *
 * @param {string} id - The ID of the product to get.
 *
 * @throws {Error} If no products are found.
 * @throws {Error} If the product is not found.
 *
 * @returns {Object} The product object.
 */
export function getSingleProduct(id) {
  if (!products || products.length === 0) {
    throw new Error("No products found");
  }

  const product = products.find((p) => p.id === id);

  if (!product) {
    throw new Error("Product not found");
  }

  return product;
}

export function getProducts(slug) {
  const filteredProducts = products.filter((product) => product.category_slug === slug);
  return filteredProducts;
}
